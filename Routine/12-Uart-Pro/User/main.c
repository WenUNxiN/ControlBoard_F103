#include "main.h"
#include "SysTick.h"
#include "rcc.h"
#include "sys.h"
#include "Uart.h"

/*--------------------------------------------------------------
 * USART1 接收完成回调函数
 * 形参：
 *   data : 指向接收缓冲区的指针
 *   len  : 本次接收到的字节数
 * 说明：
 *   接收完成后由底层驱动自动调用，本例直接将数据回显。
 *-------------------------------------------------------------*/

void Usart1_on_recv(uint8_t* data, uint32_t len)
{
    /* 通过 printf 回显收到的字符串 */
    printf("recv1: %s\r\n", data);
}
/*--------------------------------------------------------------
 * 主函数
 *-------------------------------------------------------------*/
int main(void)
{
    /* 1. 系统时钟初始化，使能 72 MHz HSE/PLL */
    Rcc_Init();

    /* 2. GPIO 重映射：关闭 JTAG，仅保留 SWD，释放 PA15/PB3/PB4 */
    SWJGpio_Init();

    /* 3. SysTick 初始化 */
    SysTickInit();

    /* 4. USART1 初始化：波特率 115200，8 数据位，无校验，1 停止位 */
    Uart1_Init(115200);
    
    while (1)
    {
        
    }
}
